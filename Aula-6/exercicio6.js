// Exercicio 6: Escreva um algoritmo para ler as dimensoes de um retangulo (base e altura), calcular e escrever a area do retangulo.

alert("Área do Retângulo");

let base = parseFloat(prompt("Digite a base do retângulo: "));
let altura = parseFloat(prompt("Digite a altura do retângulo: "));

let area = base * altura;

alert("A área do retângulo é: " + area);

